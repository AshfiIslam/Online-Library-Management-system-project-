			<p> The  paper  titled  Online Library  Management  System  is  Library  management  software  for  monitoring and  controlling  the  transactions  in  a  library  .The  paper  "Library  Management  System"  is  developed  in  mysql,php,javascript which  mainly  focuses on basic  operations in a  library like  adding new  member, new books, and updating  new information, searching books and members and facility to borrow and return books.This  software  is  easy  to  use  for  both  beginners  and advanced users. </p>
	<p>
             The     purpose     of     the application  is  automation  of  library  ,it    provide facilities  to  student  or  member  to  search  for  the required  books  and  it  allows  the  administrator  or librarian  to  Issue  &  return  books  to  students  and can  create  &  delete  membership  of  students.  Insertion to Database Module -User friendly input screenExtracting   from   Database   module -Attractive Output ScreenReport  Generation  module -borrowed  book  list  & Available book listSearch   Facility   system -search   for  books  and members </p>
             <p>
	System Analysis is a detailed study of the  various  operations  performed  by  a  system  and their relationships within and outside of the system. Here the key question is-what all problems exist in the  present  system  what  must  be  done  to  solve  the problem  Analysis  begins  when  a  user  or  manager begins   a   study   of   the   program   using   existing system.In  our  existing  system  all  the  transaction  of  books are  done  manually,  So  taking more  time  for  a transaction  like  borrowing  a  book  or  returning  a book and also for searching of members and books. Another major disadvantage is that to preparing the list  of  books  borrowed  and  the  available  books  in the library will take more time, currently it is doing as  a  one  day  process  for  verifying  all  records.  So after conducting the feasibility study we decided to make the manual Library management system to be computerized.

